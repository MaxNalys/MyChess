package utils;

public class Coordinates {
    // TODO should be final, as you should see IDE even suggests so
    private int x;
    private int y;

    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
